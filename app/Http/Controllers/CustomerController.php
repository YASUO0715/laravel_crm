    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $method = 'GET';
        $zip_code = '7830060';
        // QIITA_URLの値を取得してURLを定義
        $url = config('zipcode.url'). '/api/search?zipcode=/'.$zip_code;

        // Client(接続する為のクラス)を生成
        $client = new Client();
        // try catchでエラー時の処理を書く
        try {
            // データを取得し、JSON形式からPHPの変数に変換
            $response = $client->request($method, $url);
            $body = $response->getBody();
            $customers = json_decode($body, false);
        } catch (\Throwable $th) {
            $customers = null;
        }
        return view('customers.index')->with(compact('customers'));

    }

